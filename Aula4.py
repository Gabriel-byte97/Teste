# Tipos int e floar
# int -> Número inteiro
# O tipo int representa qualquer número
# Positivo ou negativo. Int sem sinal é considerado positivo.
# print(11) #int
# print(-11) #int
# print(0)

# float -> Número com ponto flutuante
# O tipo float representa qualquer número
# Positivo ou negativo com ponto flutuante
# float sem sinal é considerado positivo.
# print(1.1, 1.01, 0.0, -12.1)
# o . é para separar as casas decimais de um número, já a "," é para outra coisa

# A função type mostra o tipo do valor 
print(type('Victor'))
print(type(1))
print(type(-1.12), type(0), type(1.2))